package com.Art.operationsandcyclingfunctions;

public class IncrementDecrementExample {
    public static void main(String[] args) {
        int a = 5;

        //Incrementare
        a++;
        System.out.println("Rezultat: " + a);

        //Incrementare Pre-fixata
        ++a;
        System.out.println("Rezultat: " + a);

        //Decrementare
        a--;
        System.out.println("Rezultat: " + a);

        //Decrementare Pre-fixata
        --a;
        System.out.println("Rezultat: " + a);

    }
}
