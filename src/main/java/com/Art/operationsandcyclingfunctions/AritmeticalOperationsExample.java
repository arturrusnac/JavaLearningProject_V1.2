package com.Art.operationsandcyclingfunctions;

public class AritmeticalOperationsExample {
    public static void main(String[] args) {
        int a = 10;
        int b = 15;

        //sum
        int sum = a + b;
        System.out.println("suma: " + sum);

        //substraction = scaderea
        int sub = a - b;
        System.out.println("rezult: " + sub);

        //multiplication
        int result = a * b;
        System.out.println("inmultirea:  " + result);

        //devision
        result = a / b;
        System.out.println("impartirea:  " + result);

        //the rest of division or Devision with reminder
        result = a % b;
        System.out.println("Procent:  " + result);


    }
}
